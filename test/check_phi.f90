subroutine test(mu, re, te, theta)
    real(wp), intent(in) :: mu(:), re(:), te, theta
    real(wp) :: r(size(mu))
    r = phi(mu, te, theta)

    print *, 'Testing phi  theta=',theta 
    do i=1, size(mu)
        if (r(i) == 0.0 .and. re(i) == 0.0) then
            write(*,"(A)",advance='no') "."
            cycle
        endif
        if  (abs(r(i)/re(i) - 1)>1e-8) then
            print *, 'Error', i
            cycle
        endif
        write(*,"(A)",advance='no') "."
    enddo
    print *
end subroutine
subroutine linspace(v, vmin, vmax)
    real(wp), intent(out) :: v(:)
    real(wp), intent(in) :: vmin, vmax
    real(wp) :: dv
    integer :: i, n

    n = size(v)
    dv = (vmax - vmin)/(n-1)
    v = [(vmin + dv*(i-1), i=1,n)]
end subroutine
